const AdminDashboard = ({ children }) => {
  const navigate = useNavigate();

  const { logout } = useAuth();
  useEffect(() => {
    const auth = JSON.parse(localStorage.getItem("auth"));
    if (auth?.user?.role == "user") navigate(AppRoutes.home);
  }, [navigate]);

  const [activeMenu] = useState("");
  const {
    token: { colorBgContainer },
  } = theme.useToken();

  const handleChange = (e) => {
    console.log(e.key);
    switch (e.key) {
      case "10":
        navigate(AppRoutes.admin);
        break;
      case "1":
        navigate(AppRoutes.blog);
        break;
      case "2":
        navigate(AppRoutes.category);
        break;
      case "3":
        navigate(AppRoutes.user);
        break;
      case "4":
        navigate(AppRoutes.productManagement);
        break;
      case "5":
        navigate(AppRoutes.contactManagement);
        break;
      case "6":
        navigate(AppRoutes.profile_management);
        break;
      case "7":
        navigate(AppRoutes.faqManagement);
        break;
      case "8":
        navigate(AppRoutes.orderManagement);
        break;
      case "9":
        navigate(AppRoutes.discountManagement);
        break;
      default:
        return null;
    }
  };
  return (
    <Layout
      style={{
        minHeight: "100vh",
      }}
    >
      <Sider collapsible collapsed={false}>
        <img
          src="../../public/image/blog-am-thuc.png"
          alt=""
          className="w-16 h-16 mx-auto mt-5"
        />
        <Menu
          theme="dark"
          className="py-6"
          defaultSelectedKeys={[activeMenu]}
          activeKey={activeMenu}
          mode="inline"
          onClick={(e) => handleChange(e)}
          items={items}
        />
      </Sider>
      <Layout>
        <Header
          style={{
            padding: "10px 24px",
            background: "#ccc",
          }}
        >
          <div className="flex justify-between">
            <Button
              type="default"
              className="bg-green-400 flex items-center gap-2"
              onClick={() => {
                window.location.reload();
              }}
            >
              <IoReload /> Refesh
            </Button>
            <Button
              type="primary"
              onClick={() => {
                navigate(AppRoutes.login);
                logout();
              }}
            >
              Đăng xuất
            </Button>
          </div>
        </Header>
        <Content
          style={{
            margin: "0 0",
          }}
        >
          {children}
        </Content>
        <Footer
          style={{
            textAlign: "center",
          }}
        >
          ©2023
        </Footer>
      </Layout>
    </Layout>
  );
};

export default AdminDashboard;
